           "ungerecht, da in keiner Weise das Können, sondern nur die persönliche Merkfähigkeit der Studierenden abgefragt "
           "wird. Außerdem ist Auswendiglernen viel weniger fesselnd, als in der Lage zu sein, richtige Sachverhalte zu "
           "bearbeiten beziehungsweise zu lösen\n\n."
