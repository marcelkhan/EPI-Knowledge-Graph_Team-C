           "dieses dann bei der Prüfung wiedergibt und daraufhin vergisst, was man in dem Fach gelernt hat. Dies fördert "
           "nicht den Lerneffekt und prüft Studierende nur auf ihre Fähigkeit, auswendig zu lernen. Als Beispiele fallen "
           "hier die Fächer Einführung in Betriebssysteme und Rechnerarchitekturen sowie Recht ein. Besser sind Prüfungen "
           "in welchen das Gelernte auch in Beispielaufgaben umgesetzt werden muss. Dies setzt jedoch große Veränderungen "
           "in einigen Fächern voraus, da es schwierig ist, sich bei diesen Fächern Prüfungen vorzustellen, die sich auf "
           "die Anwendung des Gelernten fokussieren.\n\n "
