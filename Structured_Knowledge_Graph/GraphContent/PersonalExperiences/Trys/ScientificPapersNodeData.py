           "alle Module abgeschlossen. Obwohl ich das Ende des Studiums erreicht hatte, habe ich noch keine reine "
           "wissenschaftliche Arbeit geschrieben. Zwar bin ich der Meinung, dass Projektarbeiten eine bessere Form "
           "der Modulprüfung sind, jedoch finde ich es suboptimal, dass meine ersten wissenschaftlichen Arbeiten mein "
           "Praxisprojekt und meine Bachelorarbeit sein werden. Ich finde es sollte spätestens im zweiten Semester "
           "eine erste und spätestens im vierten Semester eine zweite wissenschaftliche Arbeit geschrieben werden. "
           "Natürlich nicht in demselben Umfang, wie die Arbeiten am Ende des Studiums, aber das Schreiben solcher "
           "Arbeiten ist ein Unterfangen, welches nicht unterschätzt werden sollte und nicht erst am Ende eines "
           "Studiums anstehen sollte. Die Bachelorarbeit als solche kann abschreckend wirken, jedoch denke ich, "
           "dass wenn eine solche wissenschaftliche Arbeit als Modulprüfung in zwei Modulen vorher geschrieben werden "
