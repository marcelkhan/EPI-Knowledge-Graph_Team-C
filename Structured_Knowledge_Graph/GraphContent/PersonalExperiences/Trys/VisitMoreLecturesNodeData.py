TITLE=("Mehr Vorlesungen besuchen")

CONTENT=("In meiner Zeit an der TH-Köln ist mir etwas stark aufgefallen, wie schlimm man rausfällt, wenn man die Vorlesungen nicht besucht.\n"
             " Das macht nicht nur einem im Akademischen Sinn, sondern auch in einen rhythmischen Sinn.\n"
             " Der akademische Sinn ist daher, weil man weniger sich mit dem Thema beschäftigt,\n"
             " was dazu führt das man sehr leicht die Themen wieder verlernt,\n"
             " das heißt das Mann in der Klausurvorbereitung diese Themen wieder mit erhöhtem Fokus neulernen muss\n"
             " nur damit man es die Sekunde wo man aus der Klausur ist es wieder vergessen,\n"
             " was bei einem zu hohen Schwänzen das Studium etwas nutzlos macht.\n"
             " Der andere Grund ist das rhythmischen das heißt mehr Schlafrhythmus natürlich muss ich sagen,\n"
             " dass der Grund, warum ich immer Vorlesungen verpasse, am meisten Uhrzeit bedingt sind.\n"
             " Zum Beispiel Mathe 1 war immer um 8 oder 9 am Morgen,\n"
             " das heißt der Wecker klingelt immer so um 7 damit man genug Zeit hat zum Duschen frühstücken etc.\n"
             " aber es gab viele Tage wo man etwas zu lange wach war der Wecker ging losn"
             " man stellt ihn aus liegt 5 Minuten im Bett in dieser Zeit kommt dann der Punkt sich wieder schlafen zu legen\n"
             " und auf einmal ist es 13 Uhr dann kann man die folgende Nacht nicht schlafen und verpasst die nächste Vorlesung,\n"
             " weil die wieder um 9 ist.\n"
             "### Autor: Daniel Hans Gabor")