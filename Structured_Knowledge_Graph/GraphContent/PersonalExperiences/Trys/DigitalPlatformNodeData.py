TITLE = "Uneinheitliche Nutzung digitaler Lernplattformen"

CONTENT = ("Meiner Meinung nach sollte die Technische Hochschule ihre Lernplattformen leichter zugänglich machen "
           "und einheitlicher nutzen. Aktuell besitzt fast jedes Modul eine eigene Plattform, da viele Professoren "
           "eigene Labore mit individuellen Webseiten und Ressourcen betreuen. Während einige Dozierende "
           "Online-Lernplattformen sehr ausgiebig nutzen, werden sie von anderen gar nicht verwendet.\n\n"
           "Es sollte meiner Ansicht nach einheitlicher und intuitiver sein, die verschiedenen Lernangebote "
           "zu nutzen. Beispielsweise wurde im Fach Softwaretechnik fast alles über das Archi-Lab koordiniert, "
           "während im Fach Datenbanken auf das spezielle E-Learning Datenbank Portal mit eigenem Layout verwiesen "
           "wurde. Diese Fragmentierung verwirrt die Studierenden, da sie sich für jedes Modul neu orientieren "
           "müssen, wo die jeweiligen Inhalte zu finden sind.\n\n"
           "Eine zentrale ‚Hub‘-Seite der Hochschule wäre hier deutlich effizienter. Diese sollte einen "
           "unkomplizierten Zugriff auf alle Plattformen ermöglichen oder diese idealerweise direkt integrieren. "
           "Ein solches zentrales Portal könnte zudem einen Anreiz für weitere Professoren schaffen, digitale "
           "Lerninhalte anzubieten, und so zu einem einheitlicheren Gesamtangebot der Hochschule beitragen.\n\n"
           "### Autor: Milan Littfin\n")