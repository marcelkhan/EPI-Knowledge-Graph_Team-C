TITLE = "Mehr Online weniger Offline"

CONTENT = ("Aus eigener Erfahrung hat mir die Pandemie gezeigt, dass digitale Lehrformate nicht nur funktionieren, sondern sogar Vorteile bieten. "
           " Ich selbst habe erlebt, wie schwierig es sein kann, immer präzise zu den Vorlesungszeiten anwesend zu sein. "
           " besonders für Berufstätige oder Kommiliton:innen mit langen Anfahrtswegen. \n\n"
           " Ein Try wäre es, ein Hybridmodell (Präsenz + Online) dauerhaft einzuführen, um die Flexibilität für Studierende zu erhöhen. "          
           " Ein solches Modell würde es ermöglichen, aufgezeichnete Vorlesungen zum Nacharbeiten bereitzustellen, "
           " was den Lernprozess, zumindest für mich, deutlich entspannter gestalten würde.\n\n "
           " Gerade in stressigen Phasen oder bei Terminkollisionen wäre das eine enorme Erleichterung,"
           " wenn die Vorlesung zusätzlich online aufgenommen werden würde. "
           " Das würde nicht nur Zeit sparen, sondern auch die Vereinbarkeit von Studium und anderen Verpflichtungen verbessern. \n\n"           
           "### Autor: Florian Luzemann\n")