           "Bereiche vermitteln und den Studenten so die Möglichkeit geben einen besseren Einblick in die jeweiligen "
           "Bereiche zu bekommen. Wenn dies zu umfangreich sein sollte, wäre auch ein Modul denkbar, in dem jeder "
