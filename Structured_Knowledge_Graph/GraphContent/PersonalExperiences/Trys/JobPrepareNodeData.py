TITLE = "Bessere Vorbereitung für zukünftige Jobs"

CONTENT = ("Meiner Meinung nach werden im Studiengang Informatik berufsrelevante Themen noch nicht ausreichend "
           "thematisiert. Viele Stellenausschreibungen formulieren Erwartungen, die ein höheres Niveau voraussetzen, "
           "als es in den entsprechenden Modulen vermittelt wird. Ich bin der Ansicht, dass Studierende sich bereits "
           "im Studium die nötigen Kompetenzen aneignen sollten, um die gängigen Grundvoraussetzungen des "
           "Arbeitsmarktes zu erfüllen. Schließlich ist es eine Kernaufgabe des Studiums, auf die Arbeitswelt "
           "vorzubereiten.\n\n"
           "In Ausschreibungen für Werkstudierende werden beispielsweise häufig Erfahrungen mit Cloud-Technologien "
           "und Frontend-Entwicklung gefordert, die im aktuellen Curriculum kaum vorkommen. Meines Erachtens sollte "
           "in den Kursen ein stärkerer Fokus auf solche Technologien gelegt werden – oder zumindest so weit darauf "
           "aufmerksam gemacht werden, dass sich Interessierte eigenständig damit auseinandersetzen können. Zudem "
           "sollten dahinterliegende Konzepte, wie etwa Microservices, tiefergehend gelehrt werden. Zum Thema "
           "Microservices gab es beispielsweise nur ein Wahlpflichtfach, welches so schnell vollständig belegt "
           "war, dass vielen Studierenden wie auch mir der Zugang zu diesem wichtigen Wissen verwehrt blieb.\n\n"
           "### Autor: Milan Littfin\n")