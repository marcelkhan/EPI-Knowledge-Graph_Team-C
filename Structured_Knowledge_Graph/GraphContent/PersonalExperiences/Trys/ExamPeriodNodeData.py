TITLE = "Starre Prüfungszeiten"

CONTENT = ("Die aktuellen Prüfungszeiten im Studiengang Informatik BA finde ich persönlich viel zu unflexibel" 
          "und berücksichtigen individuelle Lebensumstände der Studierenden nicht ausreichend." 
          "Viele Kommiliton:innen, mich eingeschlossen, haben schon erlebt, wie schwierig es ist, sich auf eine Prüfung vorzubereiten, " 
          "wenn viele Klausuren auf einen Prüfungsphase oder sogar nur auf 1 bis 2 tage fallen." 
          "Ein Try wäre es, mehrere Prüfungstermine pro Semester anzubieten, um Kollisionen zu vermeiden. " 
          "Alternativ könnte man manche Prüfungen zu online ermöglichen, um so räumliche und zeitliche Flexibilität zu schaffen. " 
          "Diese Prüfungen könnten dann für jeden individualisiert sein und so könnte sich jeder ein eigenes Zeitfenster in einem gegebenen Zeitraum geben. " 
          "Dies würde nicht nur die Zufriedenheit der Studierenden erhöhen, sondern auch durch einen angepassten Zeitplan den stress in der Prüfungszeit reduzieren."
           "### Autor: Florian Luzemann\n")