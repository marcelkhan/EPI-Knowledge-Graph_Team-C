           "stumpf aus Vorlesung und Prüfung bestanden, sondern jene, welche mich praktisch mit dem Thema haben "
           "arbeiten lassen. Praxisnähe lässt sich auf vielen Wegen erreichen. Sei es durch Projektarbeit, "
           "interaktive Workshops oder einfach der Besprechung und Erarbeitung von wirklichen Problemen, "
           "an Stelle einer Vorlesung, bei dem die Studenten nur dasitzen und zuhören müssen. Jede dieser Maßnahmen "
           "sorgt dafür, dass das Wissen nicht einfach nur gelernt werden muss, um an einem Tag abrufbar zu sein, "
           "sondern dass es gelernt wird, um es regelmäßig so anzuwenden, wie es auch in der Praxis genutzt werden "
           "sollte. Mir ist bewusst, dass eine Umsetzung dessen nicht in allen Fächern möglich ist. In Fächern wie "
           "Mathe, BWL und Recht ist sowas eher schwer umzusetzen. Aber in Fächern wie Datenbanken, Softwaretechnik, "
           "Mensch-Computer-Interaktionen und Projektmanagement wird sowas hervorragend genutzt, was sie von Fächern "
           "wie Algorithmik und KI abhebt und deutlich interessanter und verständlicher macht. Das führt mich zu dem "
           "Fazit, dass dies weitergeführt werden sollte und in mehr Fächern, wo es möglich ist, umgesetzt werden "
           "sollte. Dabei wären bereits praktische Einheiten innerhalb einer ansonsten frontalen Vorlesung eine "
