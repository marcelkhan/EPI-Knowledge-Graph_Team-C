           " sind für mich persönlich eins der Highlights des Studiengangs und sollten immer gestärkt werden. "
           " Sie ermöglichen es einigen meiner bekannten Kommilitonen, an reale Problemstellungen zu bearbeiten und echte Berufserfahrung zu sammeln, "
           " welche meiner Meinung nach für den weiteren Verlauf der Karriere sehr wichtig sind. \n\n"
           " Diese Kooperationen steigern nicht nur die Motivation der Studenten, sondern auch die ‘Employability’ der Absolvent:innen, " 
           " was attraktiv für beide Parteien, Absolvent:inne und Unternehmen, ist. \n\n" 
           " Besonders positiv ist, dass Studierende Netzwerke knüpfen und oft direkt Jobangebote von Unternehmen erhalten. "
           " Diese Praxisnähe sollte meiner Meinung nach weiter ausgebaut werden, indem noch mehr, im besten Fall lokale, "
