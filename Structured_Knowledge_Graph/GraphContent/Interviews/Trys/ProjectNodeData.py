TITLE = "Mehr Projektarbeiten um Prüfungen zu ersetzen"

CONTENT = ("Die Befragung ergab, dass die Studierenden projektbasierte Prüfungsformen gegenüber traditionellen "
           "Klausuren bevorzugen. Der Fokus klassischer Prüfungen liegt häufig auf dem reinen Auswendiglernen, wodurch "
           "die Anwendung der vermittelten Kompetenzen oft zu kurz kommt. Dies führt dazu, dass Wissen häufig nur "
           "kurzfristig gespeichert und nach der Prüfung schnell wieder vergessen wird.\n\n"
           "Projektarbeiten bieten hingegen den Vorteil eines hohen Praxisbezugs. Dadurch sind Studierende besser "
           "in der Lage die gelehrten Inhalte zu verwenden. Außerdem werden Teamfähigkeiten der Studierenden weiter "
           "gefördert, was im Kontext des zukünftigen Berufes sehr hilfreich ist, da häufig Projekte in Team "
           "abgeschlossen werden müssen. Desweiteren ermöglichen Gruppenarbeiten, dass sich Studierende "
           "untereinander vernetzen können und sich Freundschaften bilden, was die Lernbereitschaft und die "
           "Motivation aufs Studium der Studierenden erhöht.\n\n"
           "Selbst theorielastige Fächer lassen sich durch Projekte prüfen, etwa indem Studierende gezielte "
           "Recherchen zu Vorlesungsthemen durchführen. In praxislastigen Fächern lassen sich Projektarbeiten "
           "noch besser eingliedern indem man thematisch relevante Aufträge erteilt, welche dann bis zu einer "
           "Deadline abgegeben werden müssen um bewertet zu werden. \n\n"
           "### Autor: Milan Littfin\n")